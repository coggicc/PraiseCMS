$.fn.validateUrl = function () {
    var $el = $(this);
    validate($el);

    $el.keyup(function () {
        validate($el);
    });
};

function validate($el) {
    if (!isUrlValid($el.val()) && $el.val() !== "") {
        $el.addClass("border-danger");
        $el.next(".invalid-url").remove();
        $el.after(alertError);
    } else {
        $el.removeClass("border-danger");
        $el.next(".invalid-url").remove();
    }
}

function alertError() {
    return '<span class="invalid-url text-danger" style="width: 100%;float: left;margin: 10px 0;">Please enter a valid URL. (http:// or https://)</span>';
}

